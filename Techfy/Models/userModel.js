const mongoose = require("mongoose");

//schema
const userSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
//model
const userModel = mongoose.model("users", userSchema);
module.exports = userModel;
